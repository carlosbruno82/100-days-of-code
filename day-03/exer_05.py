# Faça um programa que leia um número Inteiro e mostre na tela o seu
# sucessor e seu antecessor

n = int(input('Digite um número: '))
a = n - 1
s = n + 1
print('Analisando o valor {}, seu antecessor {} e o sucessor {}'.format(n, a, s)) 
